public class TwoIntegers 
{
	private int numone;
	private int numtwo;
	
	public TwoIntegers(int numone, int numtwo)
	{
		numone = 0;
		numtwo = 0;
	}
	
	public void TwoIntegers (int numone, int numtwo)
	{
		this.numone = numone;
		this.numtwo = numtwo;
	}
	
	public int getNumone()
	{
		return numone;
	}
	public void setNumone(int numone)
	{
		this.numone = numone;
	}
	public int getNumtwo()
	{
		return numtwo;
	}
	public void setNumtwo(int numtwo)
	{
		this.numtwo = numtwo;
	}
}
